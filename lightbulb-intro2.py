from datetime import datetime
from typing import List, Optional

def sum_light(els: List[datetime], start_watching: Optional[datetime] = datetime(1, 1, 1, 0, 0)) -> int:
    d_list = (list(filter(lambda _: (_ - start_watching).total_seconds() > 0, els)))
    if len(d_list) != len(els): d_list.insert(0, start_watching)
    return sum(map(
        lambda _: (_[1] - _[0]).total_seconds(),
        zip(*(iter(d_list),) *2)
    ))

